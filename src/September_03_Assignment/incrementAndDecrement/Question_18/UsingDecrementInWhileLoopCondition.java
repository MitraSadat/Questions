package September_03_Assignment.incrementAndDecrement.Question_18;

public class UsingDecrementInWhileLoopCondition {

    public static void main(String[] args) {

        int b = 5;
        while (b-- > 0) {
            // b-- is 4 and b > 0 is true (4 > 0)
            System.out.println(b);
        }
    }
}
