package September_03_Assignment.stringMethods.length.Question_8;

public class DynamicStringLengths {

    public static void main(String[] args) {

        String str1 = "Java";
        System.out.println("The length of the string is : " + str1.length());
        str1 += "Programming";
        System.out.println("The length of the string after concatenating it is : " + str1.length());
    }
}
