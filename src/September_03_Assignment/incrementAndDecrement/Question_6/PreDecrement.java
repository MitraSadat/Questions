package September_03_Assignment.incrementAndDecrement.Question_6;

public class PreDecrement {

    public static void main(String[] args) {

        int d = 20;
        System.out.println(d);
//      Pre-decrement: Decrements the value of the variable before using it in an expression.
//        Post-decrement: Uses the current value of the variable before decrementing it.
        System.out.println(--d);
    }
}
