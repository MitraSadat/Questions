package September_03_Assignment.casting.Question_3;

public class ChartoInt {

    public static void main(String[] args) {

        char ch = 'A';
//      Casting a char to an int in Java converts the character to its numerical Unicode code point.
//      For the character 'A', the printed result is 65, which is the Unicode code point for 'A'.
        int var = ch;
        System.out.println(var);
    }
}
